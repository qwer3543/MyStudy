
public class Ex02_for_exam {

	public static void main(String[] args) {
		
		//  연습
		
		// 1 ~ 5까지 출력
		
		for (int q = 1 ; q <= 5 ; q++ ) {
			System.out.println(q);
		}
		System.out.println("================================");
		
		// 11, 12, 13, 14, 15 출력
		
		for (int w = 11 ; w <= 15 ; w++ ) {
			System.out.println(w);
		}
		System.out.println("================================");
		
		// 1, 3, 5, 7, 9 출력
		
		for (int e = 1 ; e <= 10 ; e += 2 ) {
			System.out.println(e);
		}
		System.out.println("================================");
		
		// 5, 4, 3, 2, 1 출력
		
		for ( int r = 5 ; r >= 1 ; r-- ) {
			System.out.println(r);
		}
		System.out.println("================================");
		
		
		
		
		
		
		
		
	}

}
